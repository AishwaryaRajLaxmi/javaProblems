public class Literal {
    public static void main(String[] args){
       //Literals in java
       long l=99999999999l;
       float f=12.56f;
       double d=12.5d;
       byte b=-12;

       System.out.println(l);
       System.out.println(f);
       System.out.println(b);
       System.out.println(d);
        

    }
}
